                                            <hr class="no_line hrmargin_b_10" />
                                            <p class="big">
                                             Out of a total $3 trillion in total claims submitted by health organizations, $262 billion have been denied, averaging nearly $5 million in denials per hospital. 65% of denied claims are never resubmitted and 58% of all denials were from commercial payors, up from 54% in 2016.
                                             <br>
                                             In 2017, we found that many health organizations are overwhelmed by the time, resources and money it takes to appeal denials, and are not sure how to develop a prevention program that won't overburden their teams.
                                              </p>
                                             
                                             
                                           
                                            <!-- One Third (1/3) Column -->
                                            <div class="column one-second">
                                                <ul class="list_idea">
                                                    <li>
                                                      <b>  Pay attention & Keep a track:</b> The best thing to do is to avoid these claim denials. Many are sent back by the commercial health insurance company because the amounts are wrong, they are miscoded, or proper approvals weren't requested and received. When the health insurance company sends back an unpaid claim, they'll explain why. Your billing employees will need to check on this reason and find a way to correct it before resubmitting the claim.
                                                    </li>
                                                    <li>
                                                        <b>Ensure Insurance Coverage and Eligibility: </b> Before you schedule a patient or provide service, the front office has an obligation to verify insurance coverage and benefits eligibility so that after the claim is submitted to the payer(s), it does not get denied for such reasons. In certain cases, it may be mandatory to obtain referral or authorization number as these claims mey get denied without a valid one. Authorizations normally are active over a specific date range and may expire if not used during that time. Some authorizations cover a period of time and/or a specified number of treatments or visits.
                                                    </li>
                                                    
                                                </ul>
                                            </div>
                                            <!-- One Third (1/3) Column -->
                                            <div class="column one-second">
                                                <ul class="list_idea">
                                                    <li>
                                                        <b>File Claims On Time:</b>Every payer has different requirements for when claims must be filed. If you miss a deadline, the claim will usually be denied. Don't risk having claims denied because of a simple oversight or office backlog. Develop processes and cheatsheets to ensure every payer's deadlines are met. Use the patient schedule as the starting point to ensure that all claims have been filed and are not just lost. Establish a process that ensures that all claims submitted (even if electronically) have indeed been received by the payer. Yes, we have seen several instances where even electronic claims fall through the cracks and never reach the payer even though the clearinghouse may show a successful transmission to the payer.
                                                    </li>
                                                    <li>
                                                      <b> Stay Current with Payer Requirements: </b>Medical billing rules and regulations are constantly changing, requiring physicians and administrators to spend time and money on continuing education, software, or staff training to stay current, having a direct effect on the cash flow and profits of a practice.There are several codes that are discontinued each year, there are several other codes which are introduced each year to reflect commonly performed procedures. Your medical billing company needs to stay on top with these rules so that you do not have to.
                                                    </li>
                                                    
                                                </ul>
