                                            <p class="big">
                                               Medical credentialing is a necessary evil and there is no easy way around it. Medical credentialing is essential to starting or building a practice and is also required when you choose a new billing company. If done wrong or postponed until a month before your desired start date, it can spell cash-flow delays, scheduling nightmares and unnecessary stress.
                                            </p>
                                            <p class="big">Today, it's become more necessary than ever for physicians and healthcare providers many types (including chiropractors, nurses, psychologists, professional counselors, and many others) to be in network (AKA: "credentialed") with insurance companies. This is a necessity, in part, because more people in the United States have health insurance than ever before. And it's a necessity because people today demand that their healthcare providers accept their insurance. Put bluntly, if you're not in network with a potential patient's health insurance plan, you're at risk of losing that potential patient to a competing practice.</p>
                                            <p class="big"> The process is tedious and time consuming, especially since the standards for approving a physician's credentials varies by insurer. The National Commission for Quality Assurance (NCQA) sets the standards for which physician credential approval is based. The NCQA requires primary source verification of a number of credentials, including: education, training, licenses, board certifications, work history, insurance coverage, and malpractice claims history.
                                            </p>
                                           
