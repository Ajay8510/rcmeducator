                                            <div class="column one-second">
                                                <ul class="list_idea">
                                                    <li>
                                                      <b>  Pay attention:</b> Complete and sign all documents within five days of receipt. The credentialing time clock doesn't start ticking until the carrier(s) receive all of the documents. Missing, outdated, or incomplete information are the most common reasons for delay. Getting it right the first time means you'll get a new provider credentialed within 90 days, for most payers.
                                                    </li>
                                                    <li>
                                                        <b>Start early: </b> Most credentialing can be done within 90 days but there are some carriers that will take 120 days so plan on 150 days.
                                                    </li>
                                                    
                                                </ul>
                                            </div>
                                            <!-- One Third (1/3) Column -->
                                            <div class="column one-second">
                                                <ul class="list_idea">
                                                    <li>
                                                        <b>Gather key pieces of information:</b> Prior to starting the credentialing process you will need a physical office location, phone, fax and a physician's National Provider Identifier (NPI), and a tax identification number.
                                                    </li>
                                                    <li>
                                                      <b> Stay current with CAQH: </b>Physicians who regularly update and attest with CAQH find credentialing and re-credentialing much easier.
                                                    </li>
                                                    
