                                        <p class="big">
                                            <i>“Simple can be harder than complex: You have to work hard to get your thinking clean to make it simple.
But it’s worth it in the end because once you get there, you can move mountains.”</i>
                                        </p>

                                        <p class="big"><b><i>― Steve Jobs</i></b></p>
<p class="big">We live in an era of experiences, be services or products, and we’ve come to have high expectations for these experiences. They are becoming more complex in nature as information and technology continues to evolve. With each evolution comes a new set of unmet needs.</p> 
<p class="big">The constant evolution of the healthcare industry in the US, it has become much more critical to identify methodologies that can help us create innovative service solutions for end patients and healthcare professional.</p> 
<p class="big"><b>Design Thinking</b> is a process oriented approach towards problem solving, it increases the probability of success and breakthrough innovation to provide industry edge solutioning.</p>
<p class="big">It’s a process that starts with the business user you’re designing for and ends with new solutions that are tailor made to suit their needs. It is all about building a deep empathy with the users; generating tons of ideas; building a bunch of prototypes; sharing what you’ve made with the people you’re designing for; and eventually putting your innovative new solution out in the world.</p>
<p class="big">Since the introduction of various guidelines/policy like HIPPA, MACRA, EHR and the Digital generation that access the basic healthcare services like Medicaid, Medicare, Aetna. It is of prime importance to design solution for our end user needs and bridge the gap with solutions that can help both the business and service providers.</p>
<p class="big">To summarize, recognizing that we live in a world of uncertainty where there are rarely “right answers” is at the core of design thinking. In addition, the range of topics and applications related to Experience design thinking is vast. Therefore, this webinar will act as an introduction to the key concepts, methods, and general uses of experience design thinking in the various corporate sectors. Overall it will challenge a Complex problem and provides a process to simplify it.</p> 
<p class="big">As healthcare professionals we have to deal with various aspects of critical services to patients. The problem space can start from managing the medical staff OR dealing with a walkin patient or his family OR providing counseling service to patient & family member OR providing quality service to patients OR Handling Patient information/service delivery effective. We need to put down an innovation hat to ensure we create a solution that can do well in the given context.
 In one of my past experiences where a medical device manufacturer wanted to launch a handheld  ultrasound machine, they had to go through the design thinking process to realize how the doctors get involved with various situations of hospital rounds, emergency care, and open health care Camps. And that created an opportunity to not only think of the device but various medical gears that they can innovate on.</p>
<p class="big">On the other hand in one of the earliest applications of HIPPA for my customer we had to dig to the level of what hospital staff is involved while dealing with patients, to how various touch points that a hospital needs to understand the complexity that we may have to delve into designing a system that is not complex to understand from a Staff training perspective, but at the same time ensure compliance with various aspects of the governing policy.</p>
<p class="big">We now deal with a society where we have patients dealing with various stress situation, to technology affected medical condition, and it is continually becoming a necessity to design solution both from business and service that helps not only the patient but also the medical staff/ Hospital authorities that helps us deliver these services.</p> 
<p class="big">This design thinking seminar will allow you to put your thinking hat and see the opportunities which can help affect our healthcare system as a whole. So let's go through these sessions and see how we can enhance our solution skills.</p>
