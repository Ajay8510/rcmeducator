        <p class="big">
        <strong>Morgan is the Director of Telemedicine at Children's Mercy </strong>Kansas City where she leads the hospital's virtual health program, which presently consists of 4 regional BSN, RN facilitated telemedicine clinics offering 31 pediatric specialty services, asynchronous diagnostic interpretations, a direct to patient in home platform and eleven metro facilities headed for complete connectedness.  She provides direction and oversight for all the administrative, financial, academic, and strategic operations of Telemedicine through collaboration with hospital administration, department leadership and the medical faculty.  
        </p>
        
         <p class="big">
         Prior to assuming her position in Medical Informatics and Telemedicine, November 2012, Morgan served in the following roles at Children's Mercy: clinical systems analyst critical care lead for design, build and implementation of Cerner solutions and critical care charge and staff nurse.  Before coming to Children's Mercy Kansas City, she served as a pediatric critical care interim nurse at Robert Wood Johnson, University of New Mexico, Shady Grove Adventist and Children's National.  Morgan's career in healthcare started at Tulane University Hospital.
         </p>
         
 <p class="big">
 Morgan Waller has a Master's in Business Administration from Benedictine College and bachelor's degrees in psychology, Spanish and nursing from Pittsburgh State University.  She is the secretary for the American Telemedicine Association's (ATA) Pediatric Special Interest Group, a steering committee member for Kansas City Digital Drive (a US Ignite affiliate) and holds memberships in the American Nurses Association (ANA), Healthcare Information and Management Systems Society, the American College of Healthcare Executives and is an affiliate member of the American Academy of Pediatrics.
 </p>
 <p class="big">
 Last year, she co-authored a research publication in the <strong>Annals of Allergy, Asthma & Immunology</strong> regarding asthma outcomes of patients treated via telemedicine.  More recently, she participated in a special committee of the ATA to create the <strong>Operational Guidelines for Pediatric Telehealth</strong>.   These guidelines received official endorsement from the American Academy of Pediatrics & the National Association of Pediatric Nurse Practitioners and were published in the <strong>eHealth and Telehealth<strong> professional journal.  Last fall <strong>Current Allergy & Asthma Reports published Waller & Stotler's Telemedicine:  A Primer as part of Topical Collection on Telemedicine & Technology</strong>.  Presently, she is serving on the <strong>ANA Committee for Connected Health Policy</strong>. 
</p>
